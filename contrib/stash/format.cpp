struct ScopeWrapper {
    ScopeWrapper(std::FILE* file, size_t indent): m_indent(indent), m_file(file) {
        for (size_t i = 0; i < indent; i++)
            std::print(file, " ");
        std::println(file, "{{");
    }
    ~ScopeWrapper() {
      for (size_t i = 0; i < m_indent; i++)
          std::print(m_file, " ");
        std::println(m_file, "}},");
    }
    std::FILE* m_file;
    size_t m_indent;
};

